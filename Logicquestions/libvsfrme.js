// Feature	Library	Framework
// Control	You call the library	The framework calls your code
// Usage	You pick what to use, when	Framework defines the structure
// Flexibility	More flexible, less opinionated	Less flexible, more opinionated
// Examples	Lodash, Axios, React (sort of)	Angular, Vue, Next.js
// Code Flow	You are in charge	Framework is in charge
// Routing	Needs external library (like react-router)	Built-in
// State Management	External (e.g., Redux, Zustand)	Built-in (via services and RxJS)